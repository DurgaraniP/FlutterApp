const double defaultPadding = 16.0;

const kPrimaryColor =
    Color(0xFF6F35A5);

// Other constants can be defined here as well
const kSecondaryColor = Color(0xFF3C4046);

class LoginForm extends StatelessWidget {
  const LoginForm({
    super.key,
  });

  @override
